function mostraIdade(nome, idade) {
    return `A idade da ${nome} e ${idade}`;
}

function mostraCidade(nome, cidade) {
    return `A cidade da ${nome} e ${cidade}`;
}

function mostraHobby(nome, hobby) {
    return `O hobby da ${nome} e ${hobby}`;
}

export {
    mostraIdade,
    mostraCidade,
    mostraHobby
}